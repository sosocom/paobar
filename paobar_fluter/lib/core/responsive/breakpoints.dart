/// Material 3 Window Size Classes：作为全局唯一真相。
abstract final class Breakpoints {
  Breakpoints._();

  /// 手机竖屏
  static const double compact = 600;

  /// 手机横屏 / 小平板
  static const double medium = 840;

  /// 平板 / 小笔记本
  static const double expanded = 1240;

  /// 大屏 / 桌面
  static const double large = 1600;
}
